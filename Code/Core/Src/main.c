/**
  * @brief  Period elapsed callback in non blocking mode
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM1)
  {
    static uint16_t update_counter = 0;
    
    // PWM logic: increment pwm_counter every 1ms
    pwm_counter++;
    if (pwm_counter >= 10)
    {
      pwm_counter = 0;
    }
    
    // Bluepill LED is active low (RESET = ON, SET = OFF)
    if (pwm_counter < duty_cycle)
    {
      HAL_GPIO_WritePin(RED_GPIO_Port, RED_Pin, GPIO_PIN_RESET);
    }
    else
    {
      HAL_GPIO_WritePin(RED_GPIO_Port, RED_Pin, GPIO_PIN_SET);
    }
    
    // Update duty cycle every 100ms (every 100 interrupts)
    update_counter++;
    if (update_counter >= 100)
    {
      update_counter = 0;
      duty_cycle += step_dir;
      
      // Reverse direction if limits are reached
      if (duty_cycle >= 10)
      {
        duty_cycle = 10;
        step_dir = -1; // Start dimming
      }
      else if (duty_cycle <= 1)
      {
        duty_cycle = 1;
        step_dir = 1;  // Start rising
      }
    }
  }
}
